public class Main {
    public static void main(String[] args)
    {
        //FOR
        for(int i=1; i<=10; i++)
        {
            System.out.println(i);
        }
        System.out.println("Dongu bitti");

        //WHİLE
        int a = 1;
        while(a<10)
        {
            System.out.println(a);
            a+=2;
        }
        System.out.println("Dongu bitti");

        //DO-WHİLE
        int b = 2;
        do
        {
            System.out.println(b);
            b+=3;
        }while (b<15);
        System.out.println("Do-While donfusu bitti");

    }
}